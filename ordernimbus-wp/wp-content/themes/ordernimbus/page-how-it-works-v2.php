// ROI Calculator
document.addEventListener('DOMContentLoaded', function() {
    const annualRevenueInput = document.getElementById('annual-revenue');
    const inventoryCostInput = document.getElementById('inventory-cost');
    const stockoutRateInput = document.getElementById('stockout-rate');
    
    const inventorySavingsEl = document.getElementById('inventory-savings');
    const stockoutSavingsEl = document.getElementById('stockout-savings');
    const totalRoiEl = document.getElementById('total-roi');
    
    function calculateROI() {
        const annualRevenue = parseFloat(annualRevenueInput.value) || 0;
        const inventoryCost = parseFloat(inventoryCostInput.value) || 0;
        const stockoutRate = parseFloat(stockoutRateInput.value) || 0;
        
        // Enterprise-level savings calculations
        const inventorySavings = inventoryCost * 0.15; // 15% inventory cost reduction
        const stockoutSavings = annualRevenue * (stockoutRate / 100) * 0.7; // 70% stockout reduction
        const totalSavings = inventorySavings + stockoutSavings;
        
        // Calculate ROI based on Enterprise plan ($36K/year)
        const annualCost = 36000;
        const roi = ((totalSavings - annualCost) / annualCost) * 100;
        
        // Update display
        inventorySavingsEl.textContent = '$' + Math.round(inventorySavings).toLocaleString();
        stockoutSavingsEl.textContent = '$' + Math.round(stockoutSavings).toLocaleString();
        totalRoiEl.textContent = Math.round(Math.max(roi, 0)).toLocaleString() + '%';
    }
    
    // Add event listeners
    annualRevenueInput.addEventListener('input', calculateROI);
    inventoryCostInput.addEventListener('input', calculateROI);
    stockoutRateInput.addEventListener('input', calculateROI);
    
    // Initial calculation
    calculateROI();
});
</script>

